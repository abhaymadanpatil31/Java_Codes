
class SwitchDemo5 {
	public static void main(String [] a) {
		String scr ="Mon";
		switch(scr){
			case  "Mon":
				System.out.println("Monday");
				break;
			case "Tue":
				System.out.println("Tuesday");
				break;
			default:
				System.out.println("Invalid");	

		}
	}
}
/*this is valid from java 1.6 version for String as Switch Condition*/
